            f'<td style="padding:8px;border:1px solid #ddd;">{dept}</td>'
            f'<td style="padding:8px;border:1px solid #ddd;">{posted_label(pub_date)}</td>'
            f'<td style="padding:8px;border:1px solid #ddd;">'
            f'<a href="{apply_url}">Apply</a></td>'
            f'</tr>'
